def red_bukva(names):# определяем функцию red_bukva с аргументом names
    letters = {} # задаем пустой словарь
    for name in names: # запускаем цикл
        for i in name: # в рамках существующего цикла запускаем еще один цикл
            letters[i] = letters.get(i, 0) + 1 # добавляем значение ключа к name в словаре letters
    letters = sorted(list(letters.items()), key=lambda x: x[1]) # сортируем словарь с помощью lambda приментильно к каждому элементу  i словаря
    return letters[0][0]

