def chast_imena(names): # определяем функцию chast_imena с аргументом names
    word = {} # задаем пустой словарь
    for name in names: # запускаем цикл
        word[name] = word.get(name, 0) + 1 # добавляем значение ключа к name в словаре word
    word = list(word.items()) # возвращаем список с всеми ключами и значениями в словаре word
    word.sort(key=lambda x: x[1], reverse=True) # сортируем словарь с помощью lambda приментильно к каждому элементу словаря и определяем наболее частое по нисходящему порядку, используя reverse=True
    return word [0][0]

